    @Override
    public void onMapReady(GoogleMap googleMap) {
        mMap = googleMap;

        if (ContextCompat.checkSelfPermission(this, Manifest.permission.ACCESS_FINE_LOCATION) == PackageManager.PERMISSION_GRANTED) {
            bulidGoogleApiClient();
            mMap.setMyLocationEnabled(true);
        }
    }


    protected synchronized void bulidGoogleApiClient() {
        client = new GoogleApiClient.Builder(this).addConnectionCallbacks(this).addOnConnectionFailedListener(this).addApi(LocationServices.API).build();
        client.connect();

    }

    @Override
    public void onLocationChanged(Location location) {

        latitude = location.getLatitude();
        longitude = location.getLongitude();
        lastlocation = location;
        if (currentLocationmMarker != null) {
            currentLocationmMarker.remove();

        }
        Log.d("lat = ", "" + latitude);
        LatLng latLng = new LatLng(location.getLatitude(), location.getLongitude());
        final MarkerOptions markerOptions = new MarkerOptions();
        markerOptions.position(latLng);
        markerOptions.title("Current Location");
        markerOptions.icon(BitmapDescriptorFactory.defaultMarker(BitmapDescriptorFactory.HUE_BLUE));
        currentLocationmMarker = mMap.addMarker(markerOptions);
        mMap.moveCamera(CameraUpdateFactory.newLatLng(latLng));
        mMap.animateCamera(CameraUpdateFactory.zoomBy(10));



        mMap.setOnInfoWindowClickListener(new OnInfoWindowClickListener() {
            @Override
            public void onInfoWindowClick(Marker marker) {

                String me = String.valueOf(marker.getPosition());


                float[] results = new float[1];
                Location.distanceBetween(
                        marker.getPosition().latitude, marker.getPosition().longitude,
                        markerOptions.getPosition().latitude, markerOptions.getPosition().longitude, results);


                Float dis = results[0] / 1000;
                String web = String.valueOf(dis);
                String pname = marker.getTitle();
                Intent intent = new Intent(getApplicationContext(), Museum.class);
                intent.putExtra("name", pname);
                intent.putExtra("distance", web);
                intent.putExtra("placetype", "searched");
                startActivity(intent);
                Toast.makeText(getApplicationContext(), web + " KM ", Toast.LENGTH_LONG).show();


            }
        });

        if (client != null) {
            LocationServices.FusedLocationApi.removeLocationUpdates(client, this);
        }
    }



        private String getAddress(LatLng location) {
            Geocoder geocoder = new Geocoder(this);
            String addresstxt = "";
            List<Address> addresses = null;
            Address address = null;

            try {
                addresses = geocoder.getFromLocation(location.latitude, location.longitude, 1);

                if (null != addresses && !addresses.isEmpty()) {
                    address = addresses.get(0);
                    for (int i = 0; i < address.getMaxAddressLineIndex(); i++) {
                        addresstxt += (i == 0) ? address.getAddressLine(i) : ("\n" + address.getAddressLine(i));

                    }
                    if (latitude != 0.0 && longitude != 0.0) {
                        Toast.makeText(this, "if", Toast.LENGTH_SHORT).show();
                        // DrawLine(new LatLng(mlatitude,mlongitude));
                        PolylineOptions polylineOptions = new PolylineOptions();
                        polylineOptions.add(location)
                                .add(new LatLng(currentLocationmMarker.getPosition().latitude, currentLocationmMarker.getPosition().longitude))
                                .add(new LatLng(latitude, longitude));
                        mMap.addPolyline(polylineOptions);

                    }
                }
            } catch (IOException e) {
                e.printStackTrace();
            }

            return addresstxt;
        }







